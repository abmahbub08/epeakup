<b>For Top Up:</b><br>

<b>Q. Can I top up or recharge any mobile phones ?</b> <br>
A. At present, you can top up or recharge any prepaid or postpaid mobile phones of Bangladesh. We are constantly working on adding more countries. Please visit us frequently for more update.<br><br>

<b>Q. How long does it take for recharge to complete?</b><br>
A. Most of the time a recharge is completed within few minutes after submitting the order and completing the payment process. Rarely a recharge may take up to 30 mins to complete. Any longer, please contact us on  support@epeakup.com /  info@epeakup.com with your email address and destination mobile number.<br><br>

<b>Q. What happens, if I enter an incorrect mobile number?</b><br>
A. If the incorrect mobile number you entered is a valid mobile number then that mobile number will be topped up and in that case we cannot return that credit or refund you the payment amount. Hence you must enter the correct mobile number and the number that you actually intending to top up.<br><br>

If the incorrect mobile number you entered is NOT a valid mobile number then you will receive an email stating that you’re top up failed and in this case you would be able to email us at support@epeakup.com /  info@epeakup.comwith the correct mobile number. We will then top up the new mobile number and send you the confirmation.<br><br>

<b>Q. What happens, if I enter an incorrect mobile type (prepaid / postpaid)?</b><br>
A. Entering an incorrect mobile type will cause the top up to fail and an email would be sent to you stating that your top up failed. You can then email at support@epeakup.com /  info@epeakup.com with the correct mobile type so that we can process the order again.<br><br>

<b>Q. I have placed a flexi order to a number. Is there any waiting period to send another flexi order to the same mobile number again? </b><br>
A. Yes, there is a 20 minute waiting period to send another flexi order to the same number. If you want to send another flexi order to the same number, please wait for 20 min before placing another order for the same destination number.<br><br>
